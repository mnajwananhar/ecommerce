    <script>
        function switchTab(tab) {
            const activeTab = document.getElementById('active-tab');
            const trashedTab = document.getElementById('trashed-tab');
            const activeProducts = document.getElementById('active-products');
            const trashedProducts = document.getElementById('trashed-products');

            if (tab === 'active') {
                activeTab.classList.add('border-[#FF9C08]', 'text-[#FF9C08]');
                trashedTab.classList.remove('border-[#FF9C08]', 'text-[#FF9C08]');
                activeProducts.classList.remove('hidden');
                trashedProducts.classList.add('hidden');
            } else {
                trashedTab.classList.add('border-[#FF9C08]', 'text-[#FF9C08]');
                activeTab.classList.remove('border-[#FF9C08]', 'text-[#FF9C08]');
                trashedProducts.classList.remove('hidden');
                activeProducts.classList.add('hidden');
            }
        }

        function showDeleteModal(productId) {
            document.getElementById('deleteForm').action = "{{ url('seller/products/manage') }}/" + productId;
            document.getElementById('deleteModal').classList.remove('hidden');
        }

        function hideDeleteModal() {
            document.getElementById('deleteModal').classList.add('hidden');
        }

        function showForceDeleteModal(productId) {
            document.getElementById('forceDeleteForm').action = "{{ url('seller/products/manage') }}/" + productId +
                "/force-delete";
            document.getElementById('forceDeleteModal').classList.remove('hidden');
        }

        function hideForceDeleteModal() {
            document.getElementById('forceDeleteModal').classList.add('hidden');
        }

        // Close modals when clicking outside
        window.onclick = function(event) {
            const deleteModal = document.getElementById('deleteModal');
            const forceDeleteModal = document.getElementById('forceDeleteModal');
            if (event.target === deleteModal) {
                hideDeleteModal();
            }
            if (event.target === forceDeleteModal) {
                hideForceDeleteModal();
            }
        }

        // Hide notification after a few seconds
        document.addEventListener('DOMContentLoaded', function() {
            const notification = document.getElementById('notification');
            if (notification) {
                setTimeout(() => {
                    notification.classList.add('opacity-0');
                    setTimeout(() => {
                        notification.remove();
                    }, 500); // Match the duration of the transition
                }, 3000); // Display for 3 seconds
            }
        });
    </script>
